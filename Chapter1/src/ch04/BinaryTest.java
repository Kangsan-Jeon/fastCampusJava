package ch04;

public class BinaryTest {
    public static void main(String[] args) {
        int num = 10;
        int nNum = 0B1010;  // binary
        int oNum = 012; // octal
        int xNum = 0XA; // hexa

        System.out.println(num);
        System.out.println(nNum);
        System.out.println(oNum);
        System.out.println(xNum);
    }
}
